--------------------------------------------------------
--  DDL for Trigger CONCESSIONARIA_TRG
--------------------------------------------------------

  CREATE OR REPLACE TRIGGER "IFRCACCOM"."CONCESSIONARIA_TRG" BEFORE INSERT ON IFRCACCOM.CONCESSIONARIA 
FOR EACH ROW 
BEGIN
  <<COLUMN_SEQUENCES>>
  BEGIN
    IF :NEW.SEQ_CONCESSIONARIA IS NULL THEN
      SELECT CONCESSIONARIA_SEQ.NEXTVAL INTO :NEW.SEQ_CONCESSIONARIA FROM DUAL;
    END IF;
  END COLUMN_SEQUENCES;
END;



/
ALTER TRIGGER "IFRCACCOM"."CONCESSIONARIA_TRG" ENABLE;
