CREATE OR REPLACE TRIGGER tr_atualizacao
 BEFORE
  INSERT OR UPDATE
 ON wostatus
REFERENCING NEW AS NEW OLD AS OLD
 FOR EACH ROW
DECLARE
   CURSOR c_tab
   IS
      SELECT dat_inicial
        FROM tab_wo_amatl
       WHERE num_wo_amatl = :NEW.wonum AND dat_final IS NULL;

   v_inicial   ifrmaximo.tab_wo_amatl.dat_inicial%TYPE   := NULL;
   v_horas     ifrmaximo.tab_wo_amatl.qtd_horas%TYPE     := NULL;
   v_minutos   ifrmaximo.tab_wo_amatl.qtd_minutos%TYPE   := NULL;
BEGIN
   IF :NEW.status = 'AMATL'
   THEN
      BEGIN
         INSERT INTO tab_wo_amatl
                     (num_wo_amatl, dat_inicial, qtd_horas,
                      qtd_minutos
                     )
              VALUES (:NEW.wonum, :NEW.changedate, TO_NUMBER ('0'),
                      TO_NUMBER ('0')
                     );
      END;
   ELSE
      BEGIN
         OPEN c_tab;

         FETCH c_tab
          INTO v_inicial;

         IF NOT c_tab%NOTFOUND
         THEN
            BEGIN
               SELECT TRUNC (((:NEW.changedate - (v_inicial)) * 24), 0)
                                                                     AS horas,
                        TRUNC ((((:NEW.changedate - (v_inicial)) * 24) * 60),
                               0
                              )
                      - (  60
                         * TRUNC (((:NEW.changedate - (v_inicial)) * 24), 0)
                        ) AS minutos
                 INTO v_horas,
                      v_minutos
                 FROM DUAL;

               UPDATE tab_wo_amatl
                  SET dat_final = :NEW.changedate,
                      qtd_horas = v_horas,
                      qtd_minutos = v_minutos
                WHERE num_wo_amatl = :NEW.wonum;
            END;
         END IF;
      EXCEPTION
         WHEN NO_DATA_FOUND
         THEN
            v_horas := v_horas;
      END;
   END IF;
END;
/


