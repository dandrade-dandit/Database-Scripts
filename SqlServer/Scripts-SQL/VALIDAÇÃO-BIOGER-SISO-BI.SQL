SELECT	SIGLA_AEROPORTO
	 ,  COUNT(1) AS TOTAL
  FROM	BIOGER.dbo.BIOGER_RPE_DES
 WHERE	YEAR(DATA_HORA_EFETIVA_POUSO) = 2009
   AND	MONTH(DATA_HORA_EFETIVA_POUSO) = 5
   --AND  GRUPO_VOO = 1
 GROUP
    BY  SIGLA_AEROPORTO
 ORDER
    BY  SIGLA_AEROPORTO


SELECT	SIGLA_AEROPORTO
	 ,  COUNT(1) AS TOTAL
  FROM	BIOGER.dbo.VIW_DADOS_BIOGER
 WHERE	ANO_DES = 2009
   AND	MES_DES = 5
   AND  GRUPO_VOO = 1
 GROUP
    BY  SIGLA_AEROPORTO
 ORDER
    BY  SIGLA_AEROPORTO


SELECT	SIGLA_AEROPORTO_OPERACAO
	 ,  COUNT(1) AS TOTAL
  FROM	BIOGER.dbo.VIW_BIOGER
 WHERE	ANO_DES = 2009
   AND	MES_DES = 5
   --AND  GRUPO_VOO_DES = 1
 GROUP
    BY  SIGLA_AEROPORTO_OPERACAO
 ORDER
    BY  SIGLA_AEROPORTO_OPERACAO



SELECT	*
FROM	BIOGER.dbo.BIOGER_RPE_DES
 WHERE	YEAR(DATA_HORA_EFETIVA_POUSO) = 2009
   AND	MONTH(DATA_HORA_EFETIVA_POUSO) = 5
   AND  GRUPO_VOO = 1
   AND  SIGLA_AEROPORTO =  'SBAR'
   AND  CD_STA NOT IN (
SELECT  A.CD_STA
  FROM  ( 
SELECT	SIGLA_AEROPORTO
     ,  DAY(DATA_HORA_EFETIVA_POUSO) AS DIA
     ,  MONTH(DATA_HORA_EFETIVA_POUSO) AS MES
     ,  YEAR(DATA_HORA_EFETIVA_POUSO) AS ANO
     ,  NUMERO_VOO
     ,  MATRICULA_AERONAVE
     ,  CD_STA
  FROM	BIOGER.dbo.BIOGER_RPE_DES
 WHERE	YEAR(DATA_HORA_EFETIVA_POUSO) = 2009
   AND	MONTH(DATA_HORA_EFETIVA_POUSO) = 5
   AND  GRUPO_VOO = 1
   AND  SIGLA_AEROPORTO =  'SBAR') AS A,
(
SELECT	SIGLA_AEROPORTO_OPERACAO
     ,  DIA_DES
	 ,  MES_DES
     ,  ANO_DES
     ,  NUMERO_DES
     ,  MATRICULA_AERONAVE_DES
  FROM	BIOGER.dbo.VIW_BIOGER
 WHERE	ANO_DES = 2009
   AND	MES_DES = 5
   AND  GRUPO_VOO_DES = 1
   AND  SIGLA_AEROPORTO_OPERACAO =  'SBAR') AS B
WHERE   A.SIGLA_AEROPORTO = B.SIGLA_AEROPORTO_OPERACAO
  AND   A.DIA = B.DIA_DES
  AND   A.MES = B.MES_DES
  AND   A.ANO = B.ANO_DES
  AND   A.NUMERO_VOO = B.NUMERO_DES
  AND   A.MATRICULA_AERONAVE = B.MATRICULA_AERONAVE_DES
)


