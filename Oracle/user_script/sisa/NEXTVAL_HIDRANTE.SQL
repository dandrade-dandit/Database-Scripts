CREATE OR REPLACE FUNCTION NEXTVAL_HIDRANTE(P_VALOR NUMBER) RETURN NUMBER IS
   V_SEQ_HIDR CAD_HIDRANTE.SEQ_HIDRANTE%TYPE;
BEGIN
   IF P_VALOR IS NULL THEN
      /*SELECT SEQ_HIDRANTE.NEXTVAL INTO V_SEQ_HIDR FROM DUAL;/*
      
      SELECT NVL(MAX(SEQ_HIDRANTE), 0) + 1 
      INTO V_SEQ_HIDR
      FROM CAD_HIDRANTE;
      
      RETURN V_SEQ_HIDR;
   ELSE
      RETURN P_VALOR;
   END IF;
END;