CREATE OR REPLACE FUNCTION NEXTVAL_TIPO_DETETOR(P_VALOR NUMBER) RETURN NUMBER IS
   V_SEQ_DETETOR TAB_TIPO_DETETOR.SEQ_TIPO_DETETOR%TYPE;
BEGIN
   IF P_VALOR IS NULL THEN
      /*SELECT SEQ_TIPO_DETETOR.NEXTVAL INTO V_SEQ_DETETOR FROM DUAL;*/
      
      SELECT NVL(MAX(SEQ_TIPO_DETETOR), 0) + 1 
      INTO V_SEQ_DETETOR
      FROM TAB_TIPO_DETETOR; 
      
      RETURN V_SEQ_DETETOR;
   ELSE
      RETURN P_VALOR;
   END IF;
END;