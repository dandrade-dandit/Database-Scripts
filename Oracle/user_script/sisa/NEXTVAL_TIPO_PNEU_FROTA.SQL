CREATE OR REPLACE FUNCTION NEXTVAL_TIPO_PNEU_FROTA(P_VALOR NUMBER) RETURN NUMBER IS
   V_SEQ_PNEU TAB_TIPO_PNEU_FROTA.SEQ_TIPO_PNEU%TYPE;
BEGIN
   IF P_VALOR IS NULL THEN
      /*SELECT SEQ_TIPO_PNEU.NEXTVAL INTO V_SEQ_PNEU FROM DUAL;*/
      
      SELECT NVL(MAX(SEQ_TIPO_PNEU), 0) + 1 
      INTO V_SEQ_PNEU
      FROM TAB_TIPO_PNEU_FROTA;
      
      RETURN V_SEQ_PNEU;
   ELSE
      RETURN P_VALOR;
   END IF;
END;