CREATE OR REPLACE FUNCTION NEXTVAL_MOTIVO_MOV_EQUIP(P_VALOR NUMBER) RETURN NUMBER IS
   V_SEQ_MOTIVO TAB_MOTIVO_MOV_EQUIP.SEQ_MOTIVO_MOVIMENTACAO%TYPE;
BEGIN
   IF P_VALOR IS NULL THEN
      /*SELECT SEQ_MOTIVO_MOV_EQUIP.NEXTVAL INTO V_SEQ_MOTIVO FROM DUAL;*/
      
      SELECT NVL(MAX(SEQ_MOTIVO_MOVIMENTACAO), 0) + 1 
      INTO V_SEQ_MOTIVO
      FROM TAB_MOTIVO_MOV_EQUIP;
      
      RETURN V_SEQ_MOTIVO;
   ELSE
      RETURN P_VALOR;
   END IF;
END;