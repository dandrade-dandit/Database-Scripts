CREATE OR REPLACE FUNCTION NEXTVAL_FABRICANTE_FROTA(P_VALOR NUMBER) RETURN NUMBER IS
   V_SEQ_FABRICANTE TAB_FABRICANTE_FROTA.SEQ_FABRICANTE%TYPE;
BEGIN
   IF P_VALOR IS NULL THEN
      /*SELECT SEQ_FABRICANTE_FROTA.NEXTVAL INTO V_SEQ_FABRICANTE FROM DUAL;*/
      
      SELECT NVL(MAX(SEQ_FABRICANTE), 0) + 1 
      INTO V_SEQ_FABRICANTE
      FROM TAB_FABRICANTE_FROTA;
      
      RETURN V_SEQ_FABRICANTE;
   ELSE
      RETURN P_VALOR;
   END IF;
END;