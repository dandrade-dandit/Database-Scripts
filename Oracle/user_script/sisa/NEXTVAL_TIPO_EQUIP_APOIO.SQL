CREATE OR REPLACE FUNCTION NEXTVAL_TIPO_EQUIP_APOIO(P_VALOR NUMBER) RETURN NUMBER IS
   V_SEQ_EQUIP TAB_TIPO_EQUIP_APOIO.SEQ_EQUIPAMENTO_APOIO%TYPE;
BEGIN
   IF P_VALOR IS NULL THEN
      /*SELECT SEQ_TIPO_EQUIP_APOIO.NEXTVAL INTO V_SEQ_EQUIP FROM DUAL;*/
      
      SELECT NVL(MAX(SEQ_EQUIPAMENTO_APOIO), 0) + 1 
      INTO V_SEQ_EQUIP
      FROM TAB_TIPO_EQUIP_APOIO;
      
      RETURN V_SEQ_EQUIP;
   ELSE
      RETURN P_VALOR;
   END IF;
END;