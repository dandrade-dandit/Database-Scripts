CREATE OR REPLACE FUNCTION NEXTVAL_CATEGORIA_FROTA(P_VALOR NUMBER) RETURN NUMBER IS
   V_SEQ_CATEGORIA TAB_CATEGORIA_FROTA.SEQ_CATEGORIA_FROTA%TYPE;
BEGIN
   IF P_VALOR IS NULL THEN
      /*SELECT SEQ_CATEGORIA_FROTA.NEXTVAL INTO V_SEQ_CATEGORIA FROM DUAL;*/
      
      SELECT NVL(MAX(SEQ_CATEGORIA_FROTA), 0) + 1 
      INTO V_SEQ_CATEGORIA
      FROM TAB_CATEGORIA_FROTA;
      
      RETURN V_SEQ_CATEGORIA;
   ELSE
      RETURN P_VALOR;
   END IF;
END;