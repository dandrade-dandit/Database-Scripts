COL DISP_NAME FORMAT A20
SELECT decode(D.inst_id, 1, (SELECT UPPER(INSTANCE_NAME) FROM GV$INSTANCE WHERE INSTANCE_NUMBER = 1), 2, (SELECT UPPER(INSTANCE_NAME) FROM GV$INSTANCE WHERE INSTANCE_NUMBER = 2), 'IFR3') as "Instância"
,   D.NAME AS DISP_NAME
,   COUNT(1) AS "TOTAL SESSION PER DISP"
FROM  GV$CIRCUIT C
,    GV$DISPATCHER D
WHERE C.DISPATCHER = D.PADDR
GROUP BY D.INST_ID
,        D.NAME
ORDER BY 1, 2
/
@T
