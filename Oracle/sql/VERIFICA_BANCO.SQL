SELECT 'VARIFICAR SE BANCO AINDA TA FAZENDO BACKUP' AS "MENSAGEM"
FROM DUAL
/

SELECT * 
FROM v$backup
WHERE STATUS != 'NOT ACTIVE'
/

SELECT 'VARIFICAR SE EXISTE ALGUM TABLESPACE OFFLINE' AS "MENSAGEM"
FROM DUAL
/

SELECT TABLESPACE_NAME,  STATUS
FROM DBA_TABLESPACES
WHERE STATUS != 'ONLINE'
/

SELECT 'VARIFICAR SE EXISTE ALGUM SEGMENTO DE ROLLBACK OFFLINE' AS "MENSAGEM"
FROM DUAL
/

SELECT SEGMENT_NAME, TABLESPACE_NAME, STATUS
FROM DBA_ROLLBACK_SEGS
WHERE STATUS != 'ONLINE'
/