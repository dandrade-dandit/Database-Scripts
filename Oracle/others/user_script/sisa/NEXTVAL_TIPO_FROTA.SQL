CREATE OR REPLACE FUNCTION NEXTVAL_TIPO_FROTA(P_VALOR NUMBER) RETURN NUMBER IS
   V_SEQ_TIPO TAB_TIPO_FROTA.SEQ_TIPO_FROTA%TYPE;
BEGIN
   IF P_VALOR IS NULL THEN
      /*SELECT SEQ_TIPO_FROTA.NEXTVAL INTO V_SEQ_TIPO FROM DUAL;*/
      
      SELECT NVL(MAX(SEQ_TIPO_FROTA), 0) + 1 
      INTO V_SEQ_TIPO
      FROM TAB_TIPO_FROTA;
      
      RETURN V_SEQ_TIPO;
   ELSE
      RETURN P_VALOR;
   END IF;
END;
