CREATE OR REPLACE FUNCTION NEXTVAL_MODELO_FROTA(P_VALOR NUMBER) RETURN NUMBER IS
   V_SEQ_MODELO TAB_MODELO_FROTA.SEQ_MODELO%TYPE;
BEGIN
   IF P_VALOR IS NULL THEN
      /*SELECT SEQ_MODELO_FROTA.NEXTVAL INTO V_SEQ_MODELO FROM DUAL;*/
      
      SELECT NVL(MAX(SEQ_MODELO), 0) + 1 
      INTO V_SEQ_MODELO
      FROM TAB_MODELO_FROTA;
      
      RETURN V_SEQ_MODELO;
   ELSE
      RETURN P_VALOR;
   END IF;
END;