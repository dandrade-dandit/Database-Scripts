CREATE OR REPLACE FUNCTION NEXTVAL_TIPO_PORTA(P_VALOR NUMBER) RETURN NUMBER IS
   V_SEQ_PORTA TAB_TIPO_PORTA.SEQ_TIPO_PORTA%TYPE;
BEGIN
   IF P_VALOR IS NULL THEN
      /*SELECT SEQ_TIPO_PORTA.NEXTVAL INTO V_SEQ_PORTA FROM DUAL;*/
      
      SELECT NVL(MAX(SEQ_TIPO_PORTA), 0) + 1 
      INTO V_SEQ_PORTA
      FROM TAB_TIPO_PORTA;
      
      RETURN V_SEQ_PORTA;
   ELSE
      RETURN P_VALOR;
   END IF;
END;