CREATE OR REPLACE FUNCTION NEXTVAL_MOTIVO_UTIL_EQUIP(P_VALOR NUMBER) RETURN NUMBER IS
   V_SEQ_MOTIVO TAB_MOTIVO_UTILIZACAO_EQUIP.SEQ_MOTIVO_UTILIZACAO%TYPE;
BEGIN
   IF P_VALOR IS NULL THEN
      /*SELECT SEQ_MOTIVO_UTILIZACAO_EQUIP.NEXTVAL INTO V_SEQ_MOTIVO FROM DUAL;*/
      
      SELECT NVL(MAX(SEQ_MOTIVO_UTILIZACAO), 0) + 1 
      INTO V_SEQ_MOTIVO
      FROM TAB_MOTIVO_UTILIZACAO_EQUIP;
      
      RETURN V_SEQ_MOTIVO;
   ELSE
      RETURN P_VALOR;
   END IF;
END;