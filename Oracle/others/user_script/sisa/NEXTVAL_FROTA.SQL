CREATE OR REPLACE FUNCTION NEXTVAL_FROTA(P_VALOR NUMBER) RETURN NUMBER IS
   V_SEQ_FROTA CAD_FROTA.SEQ_FROTA%TYPE;
BEGIN
   IF P_VALOR IS NULL THEN
      /*SELECT SEQ_FROTA.NEXTVAL INTO V_SEQ_AREA FROM DUAL;*/

     SELECT NVL(MAX(SEQ_FROTA), 0) + 1 
     INTO V_SEQ_FROTA
     FROM CAD_FROTA;
    
     RETURN V_SEQ_FROTA;
   ELSE
      RETURN P_VALOR;
   END IF;
END;