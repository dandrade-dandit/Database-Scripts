CREATE OR REPLACE FUNCTION NEXTVAL_DETETOR(P_VALOR NUMBER) RETURN NUMBER IS
   V_SEQ_LACO CAD_DETETOR.SEQ_LACO%TYPE;
BEGIN
   IF P_VALOR IS NULL THEN
      /*SELECT SEQ_DETETOR.NEXTVAL INTO V_SEQ_LACO FROM DUAL;*/
      
      SELECT NVL(MAX(SEQ_LACO), 0) + 1 
      INTO V_SEQ_LACO
      FROM CAD_DETETOR;
      
      RETURN V_SEQ_LACO;
   ELSE
      RETURN P_VALOR;
   END IF;
END;