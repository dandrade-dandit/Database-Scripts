create table INTERNACAO (
	INT_GUI_DOC_NUMERO		NUMBER(10) NOT NULL 
,	INT_GUI_DOC_CBE_BEN_SIGLA	VARCHAR2(3) NOT NULL 
,	INT_GUI_DOC_CBE_CRE_SEQUENCIAL	NUMBER(5) NOT NULL 
,	INT_GUI_DOC_DEP_CODIGO          NUMBER(4) NOT NULL 
,	INT_GUI_NUMERO 	                NUMBER(6) NOT NULL 
,	INT_SEQUENCIAL                  NUMBER(7) NOT NULL 
,	INT_AMB_CODIGO                  NUMBER(7)
,	INT_AMB_DV                      NUMBER(1)
,	INT_DATA_INTERNACAO             DATE
,	INT_DATA_ALTA                   DATE
,	INT_IND_OBITO                   VARCHAR2(1)
,	INT_RECEM_NASCIDO               NUMBER(1)
,	INT_IND_AMBULATORIO             VARCHAR2(1)
)
/

alter table INTERNACAO 
add constraint INTERNA_PK primary key (
	INT_GUI_DOC_NUMERO
,	INT_GUI_DOC_CBE_BEN_SIGLA
,	INT_GUI_DOC_CBE_CRE_SEQUENCIAL
,	INT_GUI_DOC_DEP_CODIGO
,	INT_GUI_NUMERO
,	INT_SEQUENCIAL
)
/

alter table INTERNACAO
add constraint INTERNA_GUI_FK foreign key(
	INT_GUI_DOC_NUMERO
,	INT_GUI_DOC_CBE_BEN_SIGLA
,	INT_GUI_DOC_CBE_CRE_SEQUENCIAL
,	INT_GUI_DOC_DEP_CODIGO
,	INT_GUI_NUMERO
)
references GUIAS (
	GUI_DOC_NUMERO
,	GUI_DOC_CBE_BEN_SIGLA
,	GUI_DOC_CBE_CRE_SEQUENCIAL
,	GUI_DOC_DEP_CODIGO
,	GUI_NUMERO
)
/

CREATE PUBLIC SYNONYM INTERNACAO FOR IFRSRH.INTERNACAO
/

INSERT INTO TABELAS VALUES  ('SRHBEN', 'INTERNACAO', 'IFRSRH', 'TABELA')
/

Select 'grant ' || privilege || ' on INTERNACAO to ' || GRANTEE || ';'
from dba_tab_privs
where TABLE_NAME     = 'GUIAS_PROCEDIMENTOS'
and OWNER = 'IFRSRH';

COMMENT ON COLUMN INTERNACAO.INT_GUI_DOC_NUMERO
IS 'NÚMERO DO DOCUMENTO DE COBRANCA';

COMMENT ON COLUMN INTERNACAO.INT_GUI_DOC_CBE_BEN_SIGLA
IS 'SIGLA DO BENEFICIO';

COMMENT ON COLUMN INTERNACAO.INT_GUI_DOC_CBE_CRE_SEQUENCIAL
IS 'CÓDIGO DO CREDENCIADO';

COMMENT ON COLUMN INTERNACAO.INT_GUI_DOC_DEP_CODIGO
IS 'CÓDIGO DA DEPENDENCIA DO DOCUMENTO';

COMMENT ON COLUMN INTERNACAO.INT_GUI_NUMERO
IS 'NÚMERO DA GUIA CORRESPONDENTE';

COMMENT ON COLUMN INTERNACAO.INT_SEQUENCIAL
IS 'CÓDIGO IDENTIFICADOR DO REGISTRO';

COMMENT ON COLUMN INTERNACAO.INT_AMB_CODIGO
IS 'CÓDIGO DO SERVIÇO';

COMMENT ON COLUMN INTERNACAO.INT_AMB_DV
IS 'DIGITO VERIFICADOR DO CODIGO DO SERVIÇO';

COMMENT ON COLUMN INTERNACAO.INT_DATA_INTERNACAO
IS 'DATA DA INTERNACAO';

COMMENT ON COLUMN INTERNACAO.INT_DATA_ALTA
IS 'DATA DA ALTA';

COMMENT ON COLUMN INTERNACAO.INT_IND_OBITO
IS 'INDICA SE O EMPREGADO/DEPENDENTE  FALECEU';

COMMENT ON COLUMN INTERNACAO.INT_RECEM_NASCIDO
IS 'INFORMAÇÃO DA CONDIÇÃO DE NASCIMENTO DO RECEM-NASCIDO';

COMMENT ON COLUMN INTERNACAO.INT_IND_AMBULATORIO
IS 'INDICA SE O ATENDIMENTO FOI FEITO NO AMBULATÓRIO';