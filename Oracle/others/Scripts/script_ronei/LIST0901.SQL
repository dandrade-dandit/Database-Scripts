CREATE ROLE backupadmin IDENTIFIED BY password
GRANT CREATE SESSION, ALTER SESSION, RESTRICTED SESSION,
  MANAGE TABLESPACE, BACKUP ANY TABLE
  TO backupadmin

CREATE ROLE securityadmin IDENTIFIED BY password
GRANT CREATE SESSION, ALTER SESSION, RESTRICTED SESSION,
  CREATE USER, ALTER USER, DROP USER,
  CREATE ROLE, ALTER ANY ROLE, DROP ANY ROLE, GRANT ANY ROLE,
  GRANT ANY PRIVILEGE,
  AUDIT ANY, AUDIT SYSTEM,
  CREATE PROFILE, ALTER PROFILE, DROP PROFILE,
  ALTER RESOURCE COST,
  CREATE PUBLIC SYNONYM, DROP PUBLIC SYNONYM,
  CREATE ANY SYNONYM, DROP ANY SYNONYM,
  TO securityadmin WITH ADMIN OPTION

CREATE ROLE spaceadmin IDENTIFIED BY password
GRANT CREATE SESSION, ALTER SESSION, RESTRICTED SESSION,
  ALTER DATABASE,
  CREATE ROLLBACK SEGMENT, ALTER ROLLBACK SEGMENT,
  DROP ROLLBACK SEGMENT,
  CREATE TABLESPACE, ALTER TABLESPACE, DROP TABLESPACE
  TO spaceadmin

CREATE ROLE objectadmin IDENTIFIED BY password
GRANT CREATE SESSION, ALTER SESSION, RESTRICTED SESSION,
  ANALYZE ANY,
  CREATE ANY CLUSTER, ALTER ANY CLUSTER, DROP ANY CLUSTER,
  CREATE ANY INDEX, ALTER ANY INDEX, DROP ANY INDEX,
  CREATE ANY PROCEDURE, ALTER ANY PROCEDURE,
  DROP ANY PROCEDURE,
  CREATE ANY SEQUENCE, ALTER ANY SEQUENCE,
  DROP ANY SEQUENCE,
  CREATE ANY TABLE, ALTER ANY TABLE, DROP ANY TABLE,
  LOCK ANY TABLE, COMMENT ANY TABLE,
  CREATE ANY TRIGGER, ALTER ANY TRIGGER, DROP ANY TRIGGER,
  CREATE ANY VIEW, DROP ANY VIEW
  TO objectadmin
