SELECT 'ALTER INDEX '|| OWNER||'.'||SEGMENT_NAME||' REBUILD ONLINE TABLESPACE TSI_SELO;'
FROM DBA_SEGMENTS
WHERE OWNER = 'IFRSELO'
AND SEGMENT_TYPE = 'INDEX'
AND TABLESPACE_NAME != 'TS_INDICE';

SELECT OWNER, COUNT(*)
FROM DBA_SEGMENTS
WHERE SEGMENT_TYPE = 'INDEX'
AND TABLESPACE_NAME != 'TS_INDICE'
GROUP BY OWNER;


SELECT OWNER, TABLESPACE_NAME, COUNT(*)
FROM DBA_SEGMENTS
WHERE SEGMENT_TYPE = 'INDEX'
GROUP BY OWNER, TABLESPACE_NAME
ORDER BY 2;


SELECT 'ALTER INDEX '|| OWNER||'.'||SEGMENT_NAME||' REBUILD ONLINE TABLESPACE TSI_PESSOAL;'
FROM DBA_SEGMENTS
WHERE OWNER IN ('IFRHUMANUS')
AND SEGMENT_TYPE = 'INDEX'
AND TABLESPACE_NAME != 'TSD_INDICE';
